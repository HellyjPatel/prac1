fun main()
{
    val a: Int = 10
    val b: Float = 3.4F
    var c: String = "helly"
    var d: Char = 'H'
    val e: Boolean = false
    val f: Double = 15.70
    val g: Long = 75934035
    val h: Short = -9
    val i: Byte = 11
    println("Integer value: $a")
    println("Float value: $b")
    println("String value: $c")
    println("Character value: $d")
    println("Boolean value: $e")
    println("Double value: $f")
    println("Long value: $g")
    println("Short value: $h")
    println("Byte value: $i")
}